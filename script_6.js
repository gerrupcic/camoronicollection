    function applyDataI18n(){
      const table = dict[currentLang] || dict.es;
      nodes.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if(!key) return;
        const val = table[key];
        if(typeof val !== 'string') return;
        if(val.includes('<')) el.innerHTML = val;
        else el.textContent = val;
      });
    }

    function apply(lang) {
      currentLang = dict[lang] ? lang : 'es';
      document.documentElement.setAttribute('lang', currentLang);
      applyDataI18n();
      updateStaticContent();
      buttons.forEach(b => b.classList.toggle('is-active', b.dataset.lang === currentLang));
      try { localStorage.setItem('cm_lang', currentLang); } catch(e){}
    }

    function getInitial() {
      try {
        const saved = localStorage.getItem('cm_lang');
        if(saved && dict[saved]) return saved;
      } catch(e){}
      const nav = (navigator.language || 'es').toLowerCase();
      if(nav.startsWith('it')) return 'it';
      if(nav.startsWith('en')) return 'en';
      return 'es';
    }

