    function fmtDateByLang(d){
      try{
        const x = new Date(d);
        if(Number.isNaN(x.getTime())) return null;
        const locale = currentLang === 'it' ? 'it-IT' : currentLang === 'en' ? 'en-US' : 'es-AR';
        return x.toLocaleDateString(locale, {year:'numeric', month:'short', day:'2-digit'});
      }catch{return null}
    }

